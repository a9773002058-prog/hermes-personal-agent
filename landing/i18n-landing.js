const keepSpace = (source, translated) => `${source.match(/^\s*/)[0]}${translated}${source.match(/\s*$/)[0]}`;

const translateLandingNode = (node, lang) => {
  if (!node.originalText) node.originalText = node.textContent;
  const key = node.originalText.trim().replace(/\s+/g, " ");
  if (!key) return;
  node.textContent = lang === "de" && Object.prototype.hasOwnProperty.call(landingTranslationsDe, key) ? keepSpace(node.originalText, landingTranslationsDe[key]) : node.originalText;
};

const applyLandingLanguage = (lang) => {
  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || ["SCRIPT", "STYLE", "NOSCRIPT"].includes(parent.tagName)) return NodeFilter.FILTER_REJECT;
      return NodeFilter.FILTER_ACCEPT;
    }
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach((node) => translateLandingNode(node, lang));

  if (!document.documentElement.dataset.ruTitle) document.documentElement.dataset.ruTitle = document.title;
  const ruTitle = document.documentElement.dataset.ruTitle;
  document.title = lang === "de" && landingMetaDe[ruTitle] ? landingMetaDe[ruTitle] : ruTitle;
  document.documentElement.lang = lang;
  localStorage.setItem("landingLanguage", lang);
  document.querySelectorAll("[data-landing-lang]").forEach((button) => {
    button.classList.toggle("active", button.dataset.landingLang === lang);
  });
};

const createLandingLanguageSwitch = () => {
  if (document.querySelector("[data-landing-language-switch]")) return;
  const switcher = document.createElement("div");
  switcher.className = "language-switch";
  switcher.dataset.landingLanguageSwitch = "";
  switcher.innerHTML = '<button type="button" data-landing-lang="ru">Русский</button><button type="button" data-landing-lang="de">Deutsch</button>';
  const headerLinks = document.querySelector(".header-links");
  if (headerLinks) headerLinks.appendChild(switcher);
  else document.body.prepend(switcher);
  switcher.addEventListener("click", (event) => {
    const button = event.target.closest("[data-landing-lang]");
    if (button) applyLandingLanguage(button.dataset.landingLang);
  });
};

document.addEventListener("DOMContentLoaded", () => {
  createLandingLanguageSwitch();
  applyLandingLanguage(localStorage.getItem("landingLanguage") || "ru");
});
